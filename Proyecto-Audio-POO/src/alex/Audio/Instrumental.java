package alex.Audio;

public class Instrumental extends Musica{
    private String album;
    private String tipoDePresentacion;

    public String getAlbum() {
        return album;
    }

    public void setAlbum(String album) {
        this.album = album;
    }

    public String getTipoDePresentacion() {
        return tipoDePresentacion;
    }

    public void setTipoDePresentacion(String tipoDePresentacion) {
        this.tipoDePresentacion = tipoDePresentacion;
    }
}
